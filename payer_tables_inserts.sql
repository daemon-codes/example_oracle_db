CREATE TABLE TRANSACTIONS(
	TRANSACTION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) NOT NULL PRIMARY KEY,
	CATEGORY_ID NUMBER NOT NULL,
	USER_ID NUMBER NOT NULL,
	RECIPIENT_ID NUMBER NOT NULL,
	TRANSACTION_TYPE_ID NUMBER NOT NULL,
	VALUE NUMBER NOT NULL,
	DESCRIPTION VARCHAR2(255),
	ADDED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    SCHEDULE_ID NUMBER,
	CONSTRAINT FK_TRANSACTION_CATEGORY_ID FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORIES(CATEGORY_ID),
	CONSTRAINT FK_TRANSACTION_USER_ID FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
	CONSTRAINT FK_TRANSACTION_RECIPIENT_ID FOREIGN KEY (RECIPIENT_ID) REFERENCES RECIPIENTS(RECIPIENT_ID),
	CONSTRAINT FK_TRANSACTION_TRANSACTION_TYPE_ID FOREIGN KEY (TRANSACTION_TYPE_ID) REFERENCES TRANSACTION_TYPES(TRANSACTION_TYPE_ID),
    CONSTRAINT FK_TRANSACTION_SCHEDULE FOREIGN KEY (SCHEDULE_ID) REFERENCES PAYMENT_SCHEDULES(SCHEDULE_ID)
);


